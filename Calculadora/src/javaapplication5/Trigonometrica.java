package javaapplication5;

public class Trigonometrica 
{
    double operador1;
    double resultado;
    void Seno()
    {
        Math.toRadians(operador1);
        resultado=Math.sin(operador1);
    }
    void Coseno()
    {
        Math.toRadians(operador1);
        resultado=Math.cos(operador1);
    }
    void Tangente()
    {
        Math.toRadians(operador1);
        resultado=Math.tan(operador1);
    }
}